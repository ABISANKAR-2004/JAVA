import java.util.*;

class Enhanced_for
{
    public static void main(String args[])
    {
        int a[]={1,2,3,52,54};
        for(int i: a)
        {
            System.out.println(i);
        }
        System.out.println("second for loop");
        int n[]={50,54,78,65};
        for(int j : n)
        {
            System.out.println(j);
        }

    }


}